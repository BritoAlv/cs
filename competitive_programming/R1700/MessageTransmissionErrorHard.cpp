vector<int> z_function(string s)
{
    int n = s.size();
    vector<int> z(n);
    int l = 0, r = 0;
    for (int i = 1; i < n; i++)
    {
        if (i < r)
        {
            z[i] = min(r - i, z[i - l]);
        }
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
        {
            z[i]++;
        }
        if (i + z[i] > r)
        {
            l = i;
            r = i + z[i];
        }
    }
    return z;
}

void Solve()
{
    string s;
    cin >> s;
    auto z = z_function(s);
    int n = sz(s);
    for (int len_s = n / 2 + 1; len_s < n; len_s++)
    {
        if (z[n - len_s] == len_s)
        {
            yesno(1);
            for (int j = n - len_s; j < n; j++)
            {
                cout << s[j];
            }
            return;
        }
    }
    yesno(0);
}

